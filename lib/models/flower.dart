class Flower {
  final String name;
  final String sciName;
  final String kingdom;
  final String family;
  final String genus;
  final String localName;
  final String description;
  final String url;

  Flower({
    required this.name,
    required this.sciName,
    required this.kingdom,
    required this.family,
    required this.genus,
    required this.localName,
    required this.description,
    required this.url,
  });
}
